    public void initPerso() {
        tab = newTable();

        for(int y=0;y <= tab.length - 1;y++){
            for(int x=0;x <= tab[y].length - 1;x++){
                if(tab[y][x] == 4){
                    positionX = x;
                    positionY = y;
                    break;
                }
            }
        }

    }

